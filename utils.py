AZUL = "\033[34m"
MAGENTA = "\033[35m"
CYAN = "\033[36m"

# FONDOS
FONDO_ROJO = "\033[41m"
FONDO_VERDE = "\033[42m"
FONDO_AMARILLO = "\033[43m"
FONDO_AZUL = "\033[44m"
